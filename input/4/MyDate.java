package Jhomework;

public class MyDate{
    private int year;
    private int month;
    private int day;
    public MyDate(){}
    public MyDate(int y, int m, int d){
        year=y;
        month=m;
        day=d;
    }
    public int getYear(){return year;}
    public int getMonth(){return month;}
    public int getDay(){return day;}
};
